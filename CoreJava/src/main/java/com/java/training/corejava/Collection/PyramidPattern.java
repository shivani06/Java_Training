package com.java.training.corejava.Collection;

public class PyramidPattern {
	public static void main(String[] args) {
	
	for(int a=5;a>0;a--) {
		int b;
		for( b=0;b<a;b++) {
			System.out.print(" ");
		}
		for(int j=b;j<5;j++) {
			System.out.print(" s ");
		}
		System.out.println(" ");
			
		}
		
	}

}
